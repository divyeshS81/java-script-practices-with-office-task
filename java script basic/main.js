let x = 5;
let y = 10;

var answer = "/";
if (answer == '+') {
    console.log(x + y);
}
else if (answer == '-') {
    console.log(x - y);
}
else if (answer == '/') {
    console.log(x / y);
}
else if (answer == '*') {
    console.log(x * y);
}
else if (answer == '%') {
    console.log(x % y);
}
else {
    console.log("invalid")
}

